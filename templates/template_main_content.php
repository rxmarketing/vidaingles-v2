<p>Utiliza <em>be allowed to</em> para hablar de permisos y reglas. "Yo tengo permiso de ir a fiestas." o "No se permite usar los celulares durante la clases." Utiliza la forma base del verbo despues de <em>allowed to</em>. Para la forma negativa usa <em>not</em> después del verbo <em>be</em>.</p>

<p>Ejemplos:</p>

<ul class="example-lists">
 <li>I am allowed to have friends over. <span class="tradu">Tengo permiso de traer amigos a casa. </span></li>
 <li>You are not allowed to wear jeans at school. <span class="tradu">No se permite llevar pantalones de mezclilla a la escuela.</span></li>
 <li>Is she allowed to come home late? <span class="tradu">&iquest;Tiene ella permiso de llegar tarde?</span></li>
</ul>

<p>Utiliza <em>you</em> para hablar en general.</p>
<ul class="example-lists">
	<li><code>You</code> are not <code>allowed to</code> chew gum at school. <span class="tradu">No se <code>permite</code> mascar chicle en la escuela. (nadie tiene permiso) </span></li>
</ul>

<h4>Estructura:</h4>
<ul class="example-lists">
	<li>(+) <code>sujeto + be + allowed to + verbo + complemento.</code></li>
 <li>(-) <code>sujeto + be + not + allowed to + verbo + complemento</code>.</li>
 <li>(?) <code>Be + sujeto + allowed to + verbo + complemento?</code></li>
 <li>(=) <code>Yes, + sujeto (pronombre personal) + be.</code></li>
 <li>(=) <code>No, + sujeto (pronombre personal) + be + not.</code></li>
</ul>1

<p>Tambien puedes usar el verbo modal <em>can</em> para hablar de permisos.</p>

<ul class="example-lists">
	<li>You're not allowed to wear jeans. <span class="tradu">No se permite usar jeans.</span></li>
 <li>You can't wear jeans. <span class="tradu">No se permite usar jeans.</span></li>
 <li>You're not allowed to park here. <span class="tradu">No esta permitido estacionarse aqui.</span></li>
 <li>You can't park here. <span class="tradu">No esta permitido estacionarse aqui.</span></li>
 <li>Are you allowed to smoke here? No, you're not.<span class="tradu">&iquest;Esta permitido fumar aqui? No se permite.</span></li>
</ul>





<h4>Forma positiva.</h4>
<p>Estructura: <code>sujeto + be + complemento</code>. Ejemplos:</p>
<ul class="example-lists">
	<li>She <code>is</code> Brenda. <span class="tradu">Ella <code>es</code> Brenda.</span></li>
 <li>I <code>am</code> in the kitchen. <span class="tradu">Yo <code>estoy</code> en la cocina.</span></li>
 <li>They <code>are</code> 22 years old. <span class="tradu">Ellos <code>tienen</code> 22 años de edad.</span></li>
 <li>It <code>is</code> cold today. <span class="tradu">Hoy <code>hace</code> frio.</span></li>
</ul>

<h4>Forma negativa</h4>
<p>Estructura: <code>sujeto + be + not + complemento</code>. Ejemplos:</p>

<ul class="example-lists">
	<li>Maribel <code>is not</code> my sister. <span class="tradu">Maribel <code>no es</code> mi hermana.</span></li>
	<li>I <code>am not</code> at work. <span class="tradu">Yo <code>no estoy</code> en el trabajo.</span></li>
	<li>Alex and Jim <code>are not</code> here. <span class="tradu">Alex y Jim <code>no estan</code> aqui.</span></li>
 <li>It <code>is not </code> hot today. <span class="tradu"><code>No hace</code> calor hoy.</span></li>
</ul>

<h4>Forma interrogativa</h4>
<p>Estructura: <code>Be + sujeto + complemento?</code>. Ejemplos:</p>

<ul class="example-lists">
	<li><code>Is</code> she your wife? <span class="tradu">¿<code>Es</code> ella tu esposa?</span></li>
	<li><code>Am</code> I the winner? <span class="tradu">¿<code>Soy</code> yo el ganador?</span></li>
	<li><code>Are</code> they your relatives? <span class="tradu">¿<code>Son</code> ellos tus parientes?</span></li>
	<li><code>Is</code> it cold outside? <span class="tradu">¿<code>Hace</code> frio afuera?</span></li>
</ul>

