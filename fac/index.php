        <h4>Modal verbs <em>may, might, could, must, can</em></h4>
        <p class="lead">Expresar <i>posibildad en el presente o futuro</i>.</p>
        <p>Use <strong>may, might, could + infinitivo</strong> para decir que algo <i>es posible</i>. Ejemplo:</p>
        <ul>
          <li>Justin <strong>may/might/could leave </strong> his job soon. <span
                    class="tradu">(Justin <em>podria dejar</em>  su trabajo pronto.)</span></li>
        </ul>
        <p>Usa <strong>must + be</strong> para expresar que <i>estas seguro</i> de algo. Ejemplo:</p>
        <ul>
          <li>You <strong>must be</strong> Tina's brother! <span class="tradu">(Tu <em>debes ser</em>  el hermano de Tina!)</span>
          </li>
        </ul>
        <p>Usa <strong>can't + be</strong> para expresar que algo <i>es imposible</i>. Ejemplo:</p>
        <ul>
          <li>This <strong>can't be</strong> Rob's car. He's got a Fiat, not a Renault. <span class="tradu">(Este <em>no puede ser</em> el coche de Rob. El tiene un Fiat, no un Renault.)</span>
          </li>
        </ul>
        <p>Use <strong>might + infinitive</strong> para expresar que algo es <i>menos probable que suceda.</i> Ejemplo:
        </p>
        <ul>
          <li>We <strong>might go</strong> to London this weekend. <span
                    class="tradu">(Nosotros <em>tal vez vayamos</em> a Londres este fin de semana.)</span></li>
        </ul>
        <p></p>
        <div class="alert alert-warning">
          <strong>NOTE: </strong>Use <strong>may, might, could, must, can't + infinitive</strong> of the verb to express
                                 possibility in the <i>present</i> or <i>future</i>.
        </div>
        <p class="lead">Expressing <em>past possibility</em></p>
        <p>Usa <strong>may/might/must/can't + have + pasado participio</strong> del verbo para expresar posibilidad en
           el <i>pasado</i>.</p>
        <p>Usa <strong>could have + pasado participio</strong> para expresar capacidad no realizada en el pasado.
           Ejemplo: </p>
        <ul>
          <li>Jim <em>could have sent</em> us a text message. <span class="tradu">Jim <em>pudo habernos enviado</em> un mensaje de texto.</span>
          </li>
        </ul>
        <p>Usa <strong>must have + past participle</strong> para expresar <i>posibilidad</i> o <i>certidumbre</i> sobre
           el pasado. Ejemplo:</p>
        <ul>
          <li>The plane <strong>must have landed</strong> early.
            <small>posibilidad</small>
            <span class="tradu">(El avion <em>debio haber aterrizado</em> antes de tiempo.)</span></li>
          <li>He <strong>must have gone out</strong>. His car isn't in the garage.
            <small>certidumbre</small>
            <span class="tradu">(El <em>debio haber salido</em>. Su carro no está en la cochera.)</span></li>
        </ul>
        <p>Usa <strong>can't have + pasado participio</strong> para expresar que algo <i>no fué posible</i>.</p>
        <ul>
          <li>Lisa <strong>can't have left</strong>. Her car is in the garage. <span class="tradu">(Lisa <em>no pudo haber salido</em>. Su coche esta en la cochera.)</span>
          </li>
        </ul>
