		<h2>Present simple <em>passive</em></h2>
		<p>Usa la <i>voz pasiva</i> cuando el objeto o persona que hace la acción no es importante, se desconoce o es obvio. Usa el verbo <i>be (am, is, are) + verbo en pasado participio</i>.</p>
		<p>Ejemplos:</p>
		<ul>
			<li><small class="text-muted">(voz activa)</small> They <span class="verbo">make</span> <span class="object">delicious Kibis</span> for lunch.
			<br /><small>(voz pasiva)</small> <span class="object">Delicious Kibis</span> <span class="verb-be">are</span> <span class="verbo">made</span> for lunch.</li>
			<li><small>(voz activa)</small> They don't <span class="verbo">speak</span> <span class="object">Spanish</span> in Brazil.
			<br /><small>(voz pasiva)</small> <span class="object">Spanish</span> <span class="verb-be">is</span> not <span class="verbo">spoken</span> in Brazil.</li>
		</ul>
		<p>Usa <em>by</em> para introducir la persona u objeto que hace la acción.</p>
		<p>Ejemplos:</p>
		<ul>
			<li>Susie <span class="verbo">bakes</span> <span class="object">delicious pastries</span>. <br />
			<span class="object">Delicious pastries</span> <span class="verb-be">are</span> <span class="verbo">baked</span> <em>by</em> Susie.</li>
		</ul>
		
