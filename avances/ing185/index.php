			<p>Usa <strong>would/wouldn't like to + verbo</strong> para hablar de preferencias. Ejemplos:</p>

			<ul>
				<li>I <em>would like to read</em> a good book. <br />
				<small>(Me <strong><i>gustaría leer</i></strong> un buen libro.)</small></li>
				<li>Brenda wouldn't like to be lawyer.<br />
				<small>(A Brenda <strong><i>no le gustaría ser</i></strong> abogado.)</small></li>
				<li>Would you like to go to Animaya?<br />
				<small>(¿Te <strong><i>gustaría ir</i></strong> a Animaya?)</small></li>
				<li>Yes, I would. / No, I wouldn't.</li>
			</ul>


