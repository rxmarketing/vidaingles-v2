            <p class="lead">Memorizará todas las preposiciones de lugar.</p>

            <ul>
                <li><em>in</em> denota ubicacion de un objeto dentro de otro, que lo contine. <em>in, inside</em> son
                    equivalentes.
                </li>
                <li><em>on</em> denota ubicación de un objeto sobre otro, en contacto con él. <em>on</em> precede al
                    nombre del objeto que sirve de base.
                </li>
                <li><em>at</em> no denota que un objeto, persona o cosa, esté contenido en otro (como <em>in</em>), no
                    sostenido por otro (como <em>on</em>), sino que está simplemente allí, en un punto determinado. <em>at</em>
                    precede al nombre del objeto punto de ubicación.
                </li>
                <li><em>outside</em> indica ubicación de un objeto fuera de otro, exterior a otro, pero sin indicar
                    ningún punto preciso de relación.
                </li>
                <li><em>Beside</em> indica ubicación de un objeto al lado de otro.</li>
                <li>
                    <em>in front of</em> indica ubicación de un objeto en relación frontal con otro. <em>The car is in
                        front of the house.</em>
                </li>
                <li>
                    <em>behind</em> indica ubicación de un objeto detrás de otro. <em>The sofa is behind the table.</em>
                </li>
                <li>
                    <em>above</em> indica ubicación de un objeto en nivel superior a otro. <em>The calendar is above the
                        switch.</em>
                </li>
                <li>
                    <em>below</em> indica ubicación de un objeto en nivel inferior a otro. <em>The keyhole is below the
                        doorknob.</em>
                </li>
                <li>
                    <em>over</em> indica ubicación de un objeto por encima de otro, sin contacto entre ambos
                    generalmente. <em>The clouds are oveer the mountains.</em>
                </li>
                <li>
                    <em>under</em> indica ubicación de un objeto por debajo de otro, con el cual puede estar en contacto
                    o no. <em>The cat is under the chair. The saucer is under the cup.</em>
                </li>
                <li><em>beside, outside, in front of, in back of, behind, above, below, over, under</em> preceden al
                    objeto con relación al cual se indica ubicación. <em>beside</em> the door, <em>outside</em> the
                    house, etc...
                </li>
            </ul>

            <h2>Modulo 6</h2>
            <p class="lead">Memorizará preposiciones de lugar en este módulo. em</p>

            <ul>
                <li>
                    <em>between</em> indica ubicación de un objeto en medio de otros dos. <em>The child is between his
                        parents.</em>
                </li>
                <li><em>among</em> indica ubicación de un objeto entre otros varios, sin precisar su posición con
                    relación a ninguno de ellos. <em>The child is among his friends.</em> between y among preceden a los
                    nombres de los objetos con relación a los cuales se hace la ubicación.
                </li>
                <li>
                    <em>across</em> indica ubicación de un objeto al otro lado de un espacio determinado. <em>The tree
                        is across the street.</em>
                </li>
                <li><em>along</em> indica ubicación a lo largo de otro. <em>The lights are along the street.</em> across
                    y along preceden los nombre de los objetos que sirve de base para indicar la ubicación.
                </li>
                <li>
                    <em>near</em> indica ubicación de un objeto en proximidad con otro, que sirve de punto de
                    referencia. <em>The girl is near the car.</em>
                </li>
                <li><em>far from</em> indica ubicación de un objeto en relación de lejanía con otro, que sirve de punto
                    de referencia. <em>The boy is far from the car.</em> near y far son términos relativos y preceden al
                    nombre de los objetos que sirven de base para indicar la ubicación.
                </li>
                <li>
                    <em>here</em> indica el lugar donde se encuentra el hablante. Es un concepto de extensión relativa,
                    ya que puede abarcar unicamente el espacio reducido que ocupa físicamente el hablante, el ambito de
                    una habitación, de un edificio, de una zona, de una ciudad, de un país, de un continente, etc. <em>The
                        book is here.</em>
                </li>
                <li>
                    <em>there</em> indica el lugar donde no se encuentra el hablante. Puede ser el lugar donde se
                    encuentre el oyente, pero no es necesariamente. <em>The pen is there. You are there.</em>
                </li>
            </ul>

